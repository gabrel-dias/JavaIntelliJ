package poo.manipulacao;

public class Livros {
    String titulo;
    String autor;
    short anoPublicacao;
    String genero;
    short qtdPaginas;

    public Livros(String titulo, String autor, short anoPublicacao, String genero, short qtdPaginas) {
        this.titulo = titulo;
        this.autor = autor;
        this.anoPublicacao = anoPublicacao;
        this.genero = genero;
        this.qtdPaginas = qtdPaginas;
    }

}
